#!/usr/bin/env node
/* ============================================================
   expand-registry.mjs — auto-registration of the ETF universe
   Pulls the full US ETF list (with AUM) from the market-data
   site, takes the top N by assets that aren't already in the
   curated registry, scans each one's top-25 holdings for Musk
   positions, and writes data.gen.js — a machine-generated
   registry extension the site loads alongside data.js.

   Run:  node scripts/expand-registry.mjs        (TOP=700 default)
         TOP=1000 node scripts/expand-registry.mjs
   ============================================================ */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
const TOP = parseInt(process.env.TOP || "700", 10);
const UA = { headers: { "User-Agent": "muskfree-certified/1.0 (registry expansion)" } };
const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

const dataSrc = fs.readFileSync(path.join(root, "data.js"), "utf8");
const { FUNDS } = (0, eval)(`(function(){ ${dataSrc}; return { FUNDS }; })()`);
const existing = new Set(FUNDS.map((f) => f.t));

/* ---- 1. full ETF universe via the SvelteKit data endpoint ---- */
async function fetchUniverse() {
  const r = await fetch("https://stockanalysis.com/etf/__data.json?count=6000", UA);
  if (!r.ok) throw new Error("universe fetch failed: " + r.status);
  const doc = await r.json();
  const node = doc.nodes.find((n) => n && n.data && n.data.some((x) => x && typeof x === "object" && !Array.isArray(x) && "resultsCount" in x));
  const pool = node.data;
  function resolve(i, depth) {
    const v = pool[i];
    if (depth > 6) return v;
    if (Array.isArray(v)) return v.map((ix) => resolve(ix, depth + 1));
    if (v && typeof v === "object") {
      const o = {};
      for (const [k, ix] of Object.entries(v)) o[k] = resolve(ix, depth + 1);
      return o;
    }
    return v;
  }
  const rootObj = resolve(0, 0);
  const rows = Array.isArray(rootObj.data) ? rootObj.data : rootObj.data.data;
  return rows.filter((x) => x && x.s && typeof x.aum === "number");
}

async function getJSON(p) {
  try {
    const r = await fetch("https://api.stockanalysis.com/api" + p, UA);
    if (!r.ok) return null;
    const j = await r.json();
    return j && j.status === 200 ? j.data : null;
  } catch { return null; }
}

function muskScan(rows) {
  let tsla = 0, spcx = 0, coverage = 0;
  for (const h of rows || []) {
    const sym = String(h.s || "").replace(/^\$/, "").toUpperCase();
    const name = String(h.n || "").toLowerCase();
    const w = parseFloat(String(h.as || "").replace("%", "")) || 0;
    coverage += w;
    if (sym === "TSLA" || /\btesla\b/.test(name)) tsla += w;
    else if (sym === "SPCX" || /space\s?x|space exploration/.test(name)) spcx += w;
  }
  return { tsla: +tsla.toFixed(2), spcx: +spcx.toFixed(2), coverage: +coverage.toFixed(1) };
}

const universe = await fetchUniverse();
console.log(`universe: ${universe.length} ETFs`);
const targets = universe
  .sort((a, b) => b.aum - a.aum)
  .filter((r) => !existing.has(r.s.toUpperCase()) && !/\//.test(r.s))
  .slice(0, TOP);
console.log(`scanning top ${targets.length} by AUM not already curated…`);

const gen = [];
let scanned = 0, noHold = 0;
for (const r of targets) {
  const t = r.s.toUpperCase();
  const hold = await getJSON(`/symbol/e/${t}/holdings`);
  await sleep(110);
  const entry = {
    t: t,
    n: r.n,
    cat: (r.assetClass ? r.assetClass + " ETF" : "ETF"),
    aumB: +(r.aum / 1e9).toFixed(2),
  };
  if (hold && hold.holdings && hold.holdings.length) {
    const scan = muskScan(hold.holdings);
    entry.tsla = scan.tsla;
    entry.spacex = scan.spcx;
    entry.cov = scan.coverage;
    scanned++;
  } else {
    entry.tsla = 0;
    entry.spacex = 0;
    entry.noscan = 1; // no holdings feed (derivatives, some bond funds) — unverified
    noHold++;
  }
  gen.push(entry);
}

const asof = new Date().toISOString().slice(0, 10);
const header = `/* AUTO-GENERATED by scripts/expand-registry.mjs on ${asof}
   Top ${targets.length} US ETFs by AUM outside the curated registry, each
   scanned for Musk exposure via its top-25 holdings. Do not hand-edit —
   rerun the script instead. */
const GEN_ASOF = "${asof}";
const GEN_FUNDS = `;
fs.writeFileSync(path.join(root, "data.gen.js"), header + JSON.stringify(gen, null, 0) + ";\n");
console.log(`wrote data.gen.js: ${gen.length} funds (${scanned} scanned, ${noHold} without holdings feed)`);
const musky = gen.filter((g) => g.tsla + g.spacex > 0.25);
console.log(`musk-positive discovered: ${musky.length}`);
console.log("top new musky:", musky.sort((a, b) => (b.tsla + b.spacex) - (a.tsla + a.spacex)).slice(0, 8).map((g) => `${g.t} ${(g.tsla + g.spacex).toFixed(1)}%`).join(", "));
